package chapters.c1_building_blocks.destroyingObjects;

public class objectVsReference {

	/**
	 * Reference:
	 * 
	 * - a variable that has a name
	 * 
	 * -can be assigned to another reference, passed to a method, or returned
	 * from a method
	 * 
	 * - have the same size, no matter the type of the object referred
	 */

	/**
	 * Object:
	 * 
	 * - sits on the heap and has no name
	 * 
	 * - the only way to access an object it's through a variable
	 * 
	 * - cannot be assigned to another nor can an object be passed to a method
	 * or returned from a method.
	 * 
	 * - It is the object that gets garbage collected, not its reference.
	 */
}
